		<h4>Why Create This Resource?</h4>
		<p>Antique textiles provide insights into the cultures that created them by mirroring the societal preoccupations, economic conditions, and technological innovations of their era. Unfortunately, many such fabrics are inaccessible, housed in temperature-controlled museum archives to avoid deterioration from handling and exposure to light.  This project aims to fill this accessibility gap by creating digital surrogates of the fragile textiles, and making the surrogates universally accessible.</p>
		<h4>Goals</h4>
		<p>In a partnership between McGill University and Dragon & Phoenix Software Inc., a set of 4000 antique and vintage textiles will be sourced, consolidated, and digitized. The results will be placed in an open access database to benefit a range of users, including historians, digital artists, designers, and architects. Artists and researchers worldwide will gain a bank of images for incorporation into illustrations for use in fashion, costumes, textiles, interiors, websites, virtual worlds, computer games, desktop publishing, and even industrial design.</p>
